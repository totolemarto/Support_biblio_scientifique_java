package com.antoine_tommy.ndarray;

/**
 * This enum represents the operations allowed on elements in an array.
 * It can be used to specify the operation to perfom like addition or substraction.
 * The Operator enum can be extended to include more operations as needed.
 */
public enum Operator {
    /** Addition */
    ADD, 

    /** Substraction */
    MINUS, 

    /** Multiplication */
    MULTIPLY, 

    /** Division */
    DIVIDE;
}
